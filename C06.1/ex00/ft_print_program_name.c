#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	p;

	p = 0;
	if (argc < 0)
		return (0);
	while (argv[0][p] != '\0')
	{
		write(1, &argv[0][p], 1);
		p++;
	}	
	write(1, "\n", 1);
}
