#include <stdlib.h>

void	numgen(int *rows, int *x)
{
	int			i;
	const int	arr[96] = {1, 2, 3, 4, 1, 3, 2, 4, 1, 2, 4, 3,
		1, 3, 4, 2, 1, 4, 3, 2, 1, 4, 2, 3, 2, 1, 3, 4,
		2, 1, 4, 3, 2, 3, 4, 1, 2, 3, 1, 4, 2, 4, 1, 3,
		2, 4, 3, 1, 3, 1, 2, 4, 3, 1, 4, 2, 3, 2, 1, 4, 3,
		2, 4, 1, 3, 4, 1, 2, 3, 4, 2, 1, 4, 1, 2, 3, 4, 1,
		3, 2, 4, 2, 1, 3, 4, 2, 3, 1, 4, 3, 1, 2, 4, 3, 2, 1};

	i = 0;
	while (*x < 331776)
	{
		while (i < 4)
		{
			rows[i] = arr[(*x % 24) * 4 + i];
			rows[i + 4] = arr[(*x / 24) % 24 * 4 + i];
			rows[i + 8] = arr[(*x / 576) % 24 * 4 + i];
			rows[i + 12] = arr[(*x / 13824) % 24 * 4 + i];
			i++;
		}
		return ;
	}
}
